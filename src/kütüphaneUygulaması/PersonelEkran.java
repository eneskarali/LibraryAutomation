    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        boolean degissinMi = true;

        String ePostaDesen = "[\\w]+@[\\w]+\\.[\\w]+";
        String ePosta = txt_ePosta.getText();
        Pattern pattern = Pattern.compile(ePostaDesen);
        Matcher matcher = pattern.matcher(ePosta);

        if (!matcher.find() || ePosta.isEmpty() == true) {
            lbl_ePostaDegisInfo.setText("Geçerli Bir E-Posta Giriniz");
            lbl_sifreDeğistirInfo.setForeground(Color.RED);
            degissinMi = false;
        } else if (txt_ePosta.getText().equals(posta)) {
            lbl_ePostaDegisInfo.setText("Zaten Girdiğiniz E-posta Adresi Kayıtlı");
            lbl_ePostaDegisInfo.setForeground(Color.red);
            degissinMi = false;
        }

        if (degissinMi == true) {
            try {
                con = DriverManager.getConnection("jdbc:derby://localhost:1527/Kütüphane", "admin", "admin");
                pre = con.prepareStatement("UPDATE PERSONEL SET POSTA=? WHERE KADI=?");
                pre.setString(1, txt_ePosta.getText());
                pre.setString(2, aktifPersonel);
                pre.executeUpdate();
                pre.close();
                con.close();

            } catch (SQLException ex) {
                Logger.getLogger(PersonelEkran.class.getName()).log(Level.SEVERE, null, ex);
            }
            lbl_ePostaDegisInfo.setText("E-posta Değiştirildi");
            lbl_ePostaDegisInfo.setForeground(new Color(20, 97, 22));
        }
    }//GEN-LAST:event_jButton2ActionPerformed

