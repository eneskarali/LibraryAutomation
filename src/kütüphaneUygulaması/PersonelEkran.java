    private void button_yoneticiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_yoneticiActionPerformed
        String mevki = "";
        try {
            // TODO add your handling code here:
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/Kütüphane", "admin", "admin");
            pre = con.prepareStatement("SELECT MEVKI FROM PERSONEL WHERE KADI=?");
            pre.setString(1, aktifPersonel);
            rs= pre.executeQuery();
            while(rs.next()){
                mevki=rs.getObject(1).toString();
            }
            con.close();
            pre.close();
            rs.close();

        } catch (SQLException ex) {
            Logger.getLogger(PersonelEkran.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (!mevki.equals("Yönetici")) {
            JOptionPane.showMessageDialog(null, "Bu Bölüme Yalnızca Yöneci Hesapları Erişebilir!", "UYARI!", JOptionPane.WARNING_MESSAGE);
        } else {
            YoneticiEkran y = new YoneticiEkran(aktifPersonel, this, anaMenu);
            y.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_button_yoneticiActionPerformed

